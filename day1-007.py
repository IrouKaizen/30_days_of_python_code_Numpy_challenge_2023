import numpy as np

arr = np.array(
    [
        [
            [4, 5, 4, 2],
            [6, 3, 5, 1],
            [5, 2, 1, 2],
        ],
        [
            [7, 2, 3, 1],
            [6, 2, 0, 9],
            [0, 1, 9, 1],
        ],
    ]
)

#In this code, we first imported the NumPy module using the statement import numpy as np.

#Then, we created a 3-dimensional NumPy array of shape (2, 3, 4) by passing a nested list of integers to the np.array() function. The nested list contains two "layers", each with three "rows" and four "columns".

#Finally, we assigned this array to the variable arr.

#You can modify the values in the nested list to create different 3-dimensional arrays with the same shape.