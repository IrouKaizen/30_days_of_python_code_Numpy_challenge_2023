#Voici comment créer un tableau NumPy unidimensionnel de taille 5 et l'assigner à la variable "arr":

import numpy as np

arr = np.array([1, 2, 3, 4, 5])
#Ici, nous avons créé un tableau unidimensionnel de taille 5 en utilisant la fonction np.array() de NumPy
# et en passant une liste de 5 éléments comme argument. Cette liste contient les éléments du tableau.
# La variable "arr" est ensuite utilisée pour stocker la référence du tableau créé.