import numpy as np

arr = np.eye(8, dtype=np.int16)
print(arr)

#Dans ce code, nous avons importé le module NumPy avec l'instruction import numpy as np.

#Ensuite, nous avons utilisé la fonction np.eye(8, dtype=np.int16) pour créer une matrice d'identité de taille 8x8 avec un type de données int16. Cette fonction crée une matrice d'identité avec des 1 sur la diagonale principale et des 0 partout ailleurs.

#Enfin, nous avons stocké cette matrice dans la variable arr, et l'avons imprimée à la console en utilisant la fonction print().

